using System;

namespace CapaDeDatos
{
    public class CLS_Distribuidor : ConexionBase
    {

        public string Id_Distribuidor { get; set; }
        public string Nombre_Distribuidor { get; set; }
        public string Usuario { get; set; }

        public void MtdSeleccionarDistribuidor()
        {
            TipoDato _dato = new TipoDato();
            Conexion _conexion = new Conexion(cadenaConexion);

            Exito = true;
            try
            {
                _conexion.NombreProcedimiento = "SP_Distribuidor_Select";

                _conexion.EjecutarDataset();

                if (_conexion.Exito)
                {
                    Datos = _conexion.Datos;
                }
                else
                {
                    Mensaje = _conexion.Mensaje;
                    Exito = false;
                }
            }
            catch (Exception e)
            {
                Mensaje = e.Message;
                Exito = false;
            }
        }
        public void MtdInsertarDistribuidor()
        {
            TipoDato _dato = new TipoDato();
            Conexion _conexion = new Conexion(cadenaConexion);

            Exito = true;
            try
            {
                _conexion.NombreProcedimiento = "SP_Distribuidor_Insert";
                _dato.Texto = Id_Distribuidor;
                _conexion.agregarParametro(EnumTipoDato.Texto, _dato, "Id_Distribuidor");
                _dato.Texto = Nombre_Distribuidor;
                _conexion.agregarParametro(EnumTipoDato.Texto, _dato, "Nombre_Distribuidor");
                _dato.Texto = Usuario;
                _conexion.agregarParametro(EnumTipoDato.Texto, _dato, "Usuario");
                _conexion.EjecutarDataset();

                if (_conexion.Exito)
                {
                    Datos = _conexion.Datos;
                }
                else
                {
                    Mensaje = _conexion.Mensaje;
                    Exito = false;
                }
            }
            catch (Exception e)
            {
                Mensaje = e.Message;
                Exito = false;
            }
        }

        public void MtdEliminarDistribuidor()
        {
            TipoDato _dato = new TipoDato();
            Conexion _conexion = new Conexion(cadenaConexion);

            Exito = true;
            try
            {
                _conexion.NombreProcedimiento = "SP_Distribuidor_Delete";
                _dato.Texto = Id_Distribuidor;
                _conexion.agregarParametro(EnumTipoDato.Texto, _dato, "Id_Distribuidor");
                _conexion.EjecutarDataset();

                if (_conexion.Exito)
                {
                    Datos = _conexion.Datos;
                }
                else
                {
                    Mensaje = _conexion.Mensaje;
                    Exito = false;
                }
            }
            catch (Exception e)
            {
                Mensaje = e.Message;
                Exito = false;
            }
        }

    }
}
